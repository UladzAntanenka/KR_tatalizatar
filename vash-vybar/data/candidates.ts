const slugify = (value: string) =>
  value
    .toLowerCase()
    .replace(/[“”«»'’]/g, '')
    .replace(/[^a-zа-яёіўўьэюя0-9]+/gi, '-')
    .replace(/^-+|-+$/g, '');

export const candidates: Candidate[] = factions.flatMap(({ faction, names }) =>
  names.map((name, index) => ({
    id: `${slugify(faction)}-${index + 1}`,
    name,
    faction,
    listNumber: index + 1,
  }))
);

export const factionsList = factions.map((item) => item.faction);
